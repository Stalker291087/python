# range function with one argument
one_imput = range(5)

for i in one_imput:
    print(i)

# range function with two arguments
two_imput = range(5, 10)

for num2 in two_imput:
    print(num2)

# range function with three arguments
three_imput = range(1, 20, 3)

for num3 in three_imput:
    print(num3)
